export const generateChartData = (days: number = 30): ChartData[] => {
  const data: ChartData[] = [];
  const now = new Date();
  let price = 100;
  let iv = 20;
  let rv = 18;
  
  for (let i = days; i >= 0; i--) {
    const date = new Date(now);
    date.setDate(date.getDate() - i);
    
    price = price * (1 + (Math.random() - 0.48) * 0.02);
    iv = Math.max(10, Math.min(50, iv + (Math.random() - 0.5) * 2));
    rv = Math.max(8, Math.min(45, rv + (Math.random() - 0.5) * 1.5));
    
    data.push({
      time: date.toISOString().slice(5, 10),
      price: Number(price.toFixed(2)),
      iv: Number(iv.toFixed(2)),
      rv: Number(rv.toFixed(2)),
    });
  }
  
  return data;
};

// Generate mini sparkline data for each symbol
export const generateSparklineData = (points: number = 20): number[] => {
  const data: number[] = [];
  let value = 50;
  for (let i = 0; i < points; i++) {
    value = value + (Math.random() - 0.5) * 10;
    value = Math.max(10, Math.min(90, value));
    data.push(value);
  }
  return data;
};

